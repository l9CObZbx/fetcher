/**
 * Example: Shopping Cart Command Types
 *
 * Commands represent intent to change state. Each command should be
 * a simple data object with the necessary payload for the aggregate
 * to process the command.
 */
interface AddCartItemCommand {
  productId: string;
  quantity: number;
  price: number;
}

interface RemoveCartItemCommand {
  productId: string;
}

interface UpdateCartItemQuantityCommand {
  productId: string;
  newQuantity: number;
}

interface CheckoutCommand {
  paymentMethod: string;
  shippingAddress: string;
  couponCode?: string;
}

interface ClearCartCommand {}

/**
 * Example: Shopping Cart State Type
 *
 * Represents the read model for a shopping cart aggregate.
 * This is the state that gets snapshotted and queried.
 */
interface CartState {
  id: string;
  items: CartItem[];
  totalAmount: number;
  status: 'ACTIVE' | 'CHECKED_OUT' | 'ABANDONED';
  createdAt: number;
  updatedAt: number;
  checkedOutAt?: number;
}

interface CartItem {
  productId: string;
  productName: string;
  quantity: number;
  price: number;
  subtotal: number;
}

/**
 * Example: Domain Events
 *
 * Events are emitted by aggregates when processing commands.
 * They represent facts that have happened and are immutable.
 */
interface CartItemAddedEvent {
  eventType: 'CartItemAdded';
  cartId: string;
  productId: string;
  quantity: number;
  price: number;
  timestamp: number;
}

interface CartItemRemovedEvent {
  eventType: 'CartItemRemoved';
  cartId: string;
  productId: string;
  timestamp: number;
}

interface CartCheckedOutEvent {
  eventType: 'CartCheckedOut';
  cartId: string;
  orderId: string;
  paymentMethod: string;
  shippingAddress: string;
  totalAmount: number;
  timestamp: number;
}

interface CartClearedEvent {
  eventType: 'CartCleared';
  cartId: string;
  timestamp: number;
}

type CartDomainEvent =
  | CartItemAddedEvent
  | CartItemRemovedEvent
  | CartCheckedOutEvent
  | CartClearedEvent;

