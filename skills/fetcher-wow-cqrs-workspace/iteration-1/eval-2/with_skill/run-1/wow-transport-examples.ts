/**
 * Demonstrates querying the event history of an aggregate.
 */
async function example7_QueryDomainEvents(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
  });

  // Query recent events
  const recentEvents = await transport.queryEvents<CartDomainEvent>(
    'cart',
    'cart-123',
    {
      sinceTime: Date.now() - 86400000, // Last 24 hours
      limit: 100,
    },
  );

  if (recentEvents.success) {
    for (const event of recentEvents.data) {
      switch (event.eventType) {
        case 'CartItemAdded':
          console.log(`[${new Date(event.timestamp).toISOString()}] Item added: ${event.productId} x${event.quantity}`);
          break;
        case 'CartItemRemoved':
          console.log(`[${new Date(event.timestamp).toISOString()}] Item removed: ${event.productId}`);
          break;
        case 'CartCheckedOut':
          console.log(`[${new Date(event.timestamp).toISOString()}] Checked out: order ${event.orderId}`);
          break;
        case 'CartCleared':
          console.log(`[${new Date(event.timestamp).toISOString()}] Cart cleared`);
          break;
      }
    }
  }

  // Query events in a time range
  const rangeEvents = await transport.queryEvents<CartDomainEvent>(
    'cart',
    'cart-123',
    {
      sinceTime: Date.now() - 604800000, // Last week
      untilTime: Date.now() - 86400000, // Up until yesterday
      limit: 50,
    },
  );

  console.log('Events in range:', rangeEvents.data.length);
}

