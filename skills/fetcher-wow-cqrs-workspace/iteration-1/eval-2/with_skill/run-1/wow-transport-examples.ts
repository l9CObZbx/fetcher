/**
 * Demonstrates TypeScript type safety when handling command results.
 */
async function example9_TypeSafeResultHandling(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
  });

  const result = await transport.sendCommand('add_cart_item', {
    productId: 'product-123',
    quantity: 1,
  } as AddCartItemCommand);

  // TypeScript narrows the type when checking success
  if (result.success) {
    // TypeScript knows result.data is CommandResult here
    const { commandId, requestId, aggregateId, aggregateVersion, stage } = result.data;

    console.log(`Command ${commandId} on aggregate ${aggregateId} at version ${aggregateVersion}`);
    console.log(`Current stage: ${stage}`);

    // Access Wow metadata if present
    if ('boundedContext' in result.data) {
      console.log('Bounded Context:', (result.data as any).boundedContext);
    }

    if ('aggregateName' in result.data) {
      const nameCapable = result.data as AggregateNameCapable;
      console.log('Aggregate Name:', nameCapable.aggregateName);
    }

    // Check for command result payload
    if ('result' in result.data && result.data.result) {
      console.log('Result payload:', result.data.result);
    }
  } else {
    // TypeScript knows result.error is WowErrorInfo here
    console.error(`Error ${result.error?.code}: ${result.error?.message}`);
  }
}

