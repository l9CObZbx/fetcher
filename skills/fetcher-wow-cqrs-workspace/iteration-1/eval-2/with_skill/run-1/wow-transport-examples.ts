// Example 3: Sending a Command with Aggregate Targeting
// =============================================================================

/**
 * Demonstrates targeting a specific aggregate instance with a command.
 * This is necessary when you want to act on an existing aggregate.
 */
async function example3_CommandWithAggregateTargeting(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
  });

  // Target a specific cart aggregate by ID
  const result = await transport.sendCommand<RemoveCartItemCommand>(
    'RemoveCartItem',
    {
      productId: 'product-456',
    },
    {
      aggregateId: 'cart-789', // Target specific cart
      aggregateVersion: 5, // Optimistic locking - will fail if version mismatch
      waitStage: CommandStage.SNAPSHOT, // Wait until snapshot is generated
    },
  );

  if (result.success) {
    console.log('Item removed, new version:', result.data.aggregateVersion);
  } else {
    // Handle specific error cases
    if (result.error?.code === 'VERSION_CONFLICT') {
      console.error('Optimistic locking failure - the cart was modified by another process');
      // Could retry with fresh version
    } else if (result.error?.code === 'AGGREGATE_NOT_FOUND') {
      console.error('The cart does not exist');
    }
  }
}

