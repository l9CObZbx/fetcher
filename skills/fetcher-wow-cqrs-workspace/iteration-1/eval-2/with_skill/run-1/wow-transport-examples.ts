/**
 * Demonstrates querying the current state of an aggregate.
 */
async function example6_QueryAggregateState(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
  });

  // Query current state
  const currentState = await transport.queryState<CartState>(
    'cart', // aggregate name
    'cart-123', // aggregate ID
  );

  if (currentState.success) {
    const cart: CartState = currentState.data;
    console.log(`Cart ${cart.id}:`);
    console.log(`  Status: ${cart.status}`);
    console.log(`  Items: ${cart.items.length}`);
    console.log(`  Total: $${cart.totalAmount.toFixed(2)}`);
  } else {
    console.error('Failed to load cart:', currentState.error);
  }

  // Query specific version
  const versionedState = await transport.queryState<CartState>(
    'cart',
    'cart-123',
    { version: 5 },
  );

  if (versionedState.success) {
    console.log('Version 5 total:', versionedState.data.totalAmount);
  }

  // Query with partial fields (if backend supports it)
  const partialState = await transport.queryState<Partial<CartState>>(
    'cart',
    'cart-123',
    { fields: ['id', 'status'] },
  );

  if (partialState.success) {
    console.log('Partial state - ID:', partialState.data.id, 'Status:', partialState.data.status);
  }
}

