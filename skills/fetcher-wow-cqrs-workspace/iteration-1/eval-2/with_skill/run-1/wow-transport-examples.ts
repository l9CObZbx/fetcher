/**
 * Demonstrates using the QueryClientFactory for complex query patterns.
 */
async function example8_UsingQueryClientFactory(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
    tenantId: 'shop-tenant',
    ownerId: 'owner-123',
  });

  // Create a factory with default settings for the 'shop' context and 'cart' aggregate
  const factory = transport.createQueryClientFactory<CartState, 'id' | 'status' | 'totalAmount'>({
    contextAlias: 'shop',
    aggregateName: 'cart',
    resourceAttribution: 'owner',
  });

  // Create snapshot query client for state queries
  const snapshotClient = factory.createSnapshotQueryClient<CartState>();
  const currentState = await snapshotClient.singleState({
    condition: { id: 'cart-123' },
  });
  console.log('Current state:', currentState);

  // List multiple carts
  const cartList = await snapshotClient.listState({
    condition: { status: 'ACTIVE' },
    limit: 10,
  });
  console.log('Active carts:', cartList.length);

  // Paged query for large result sets
  const pagedCarts = await snapshotClient.pagedState({
    condition: { status: 'ACTIVE' },
    limit: 20,
    offset: 0,
  });
  console.log(`Showing ${pagedCarts.list.length} of ${pagedCarts.total} total`);

  // Create event stream query client for event sourcing
  const eventClient = factory.createEventStreamQueryClient<CartDomainEvent>();
  const events = await eventClient.list({
    condition: { aggregateId: 'cart-123' },
    limit: 50,
  });
  console.log('Recent events:', events.length);

  // Create state aggregate client for loading by ID
  const stateClient = factory.createLoadStateAggregateClient<CartState>();
  const cartById = await stateClient.load('cart-123');
  console.log('Loaded by ID:', cartById);

  // Load specific version
  const versionedCart = await stateClient.loadVersioned('cart-123', 5);
  console.log('Version 5:', versionedCart);

  // Create owner-specific state client for current user's carts
  const ownerStateClient = factory.createOwnerLoadStateAggregateClient<CartState>();
  const myCart = await ownerStateClient.load();
  console.log('My cart:', myCart);
}

