// Example 11: Integration with Custom Fetcher
// =============================================================================

/**
 * Demonstrates using WowTransport with a pre-configured Fetcher instance.
 * This is useful when you need custom interceptors, authentication, or logging.
 */
async function example11_CustomFetcher(): Promise<void> {
  // Import Fetcher and create custom instance
  const { Fetcher, InterceptorManager } = await import('@ahoo-wang/fetcher');

  // Create interceptor manager for custom interceptors
  const interceptors = new InterceptorManager();

  // Add a logging interceptor (request phase)
  interceptors.request.use({
    name: 'logging',
    order: 0,
    intercept: async (exchange) => {
      const start = Date.now();
      console.log(`[${new Date().toISOString()}] ${exchange.request.method} ${exchange.request.url}`);
      exchange.attributes.set('requestStartTime', start);
    },
  });

  // Add a correlation ID interceptor
  interceptors.request.use({
    name: 'correlation',
    order: 1,
    intercept: async (exchange) => {
      const correlationId = crypto.randomUUID();
      exchange.request.headers = exchange.request.headers || {};
      (exchange.request.headers as any)['X-Correlation-ID'] = correlationId;
      exchange.attributes.set('correlationId', correlationId);
    },
  });

  // Add a response logging interceptor
  interceptors.response.use({
    name: 'response-logging',
    order: 0,
    intercept: async (exchange) => {
      const start = exchange.attributes.get('requestStartTime') as number;
      const duration = Date.now() - start;
      console.log(`[${new Date().toISOString()}] Response: ${exchange.response?.status} (${duration}ms)`);
    },
  });

  // Create fetcher with custom interceptors
  const customFetcher = new Fetcher({
    baseURL: 'https://api.example.com',
    interceptors,
    timeout: 30000,
  });

  // Share the fetcher with WowTransport
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
    fetcher: customFetcher, // Reuse fetcher with custom interceptors
    tenantId: 'tenant-123',
  });

  // Commands will now include logging and correlation ID
  const result = await transport.sendCommand('do_something', { data: 'test' });
  console.log('Result:', result.success);
}

