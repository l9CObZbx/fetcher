// Example 2: Sending a Simple Command
// =============================================================================

/**
 * Demonstrates sending a basic command to add an item to a cart.
 */
async function example2_SendSimpleCommand(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
    tenantId: 'shop-tenant',
  });

  // Send a command to add an item to the cart
  // The aggregate ID will be generated by the backend if not specified
  const result = await transport.sendCommand<AddCartItemCommand>(
    'AddCartItem', // Command name - maps to aggregate command handler
    {
      productId: 'product-123',
      quantity: 2,
      price: 29.99,
    },
  );

  // Handle the response
  if (result.success) {
    // Access the command result data
    const commandResult: CommandResult = result.data;
    console.log('Command ID:', commandResult.commandId);
    console.log('Request ID:', commandResult.requestId);
    console.log('Aggregate ID:', commandResult.aggregateId);
    console.log('New Aggregate Version:', commandResult.aggregateVersion);
    console.log('Command Stage:', commandResult.stage);

    // Access command result if any
    if (commandResult.result) {
      console.log('Command Result:', commandResult.result);
    }

    // Check for command-level errors
    if ((commandResult as any).error) {
      console.error('Command error:', (commandResult as any).error);
    }
  } else {
    // Handle failure
    console.error(`Error ${result.error?.code}: ${result.error?.message}`);
    if (result.error?.details) {
      console.error('Details:', result.error.details);
    }
  }
}

