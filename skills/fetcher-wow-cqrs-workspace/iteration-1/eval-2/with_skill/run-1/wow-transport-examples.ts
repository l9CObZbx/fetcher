// Example 4: Waiting for Different Command Stages
// =============================================================================

/**
 * Demonstrates waiting for different stages of command processing.
 * Wow CQRS processes commands through multiple stages.
 */
async function example4_WaitingForStages(): Promise<void> {
  const transport = new WowTransport({
    baseURL: 'https://api.example.com/wow',
  });

  // Wait only until command is sent to the bus
  const sentResult = await transport.sendCommand(
    'AddCartItem',
    { productId: 'p1', quantity: 1, price: 10 },
    { waitStage: CommandStage.SENT },
  );

  // Wait until command is processed by aggregate
  const processedResult = await transport.sendCommand(
    'AddCartItem',
    { productId: 'p1', quantity: 1, price: 10 },
    { waitStage: CommandStage.PROCESSED },
  );

  // Wait until snapshot is generated (most common for reads after writes)
  const snapshotResult = await transport.sendCommand(
    'AddCartItem',
    { productId: 'p1', quantity: 1, price: 10 },
    { waitStage: CommandStage.SNAPSHOT },
  );

  // Wait until events are projected to read models
  const projectedResult = await transport.sendCommand(
    'AddCartItem',
    { productId: 'p1', quantity: 1, price: 10 },
    { waitStage: CommandStage.PROJECTED },
  );

  console.log('Results by stage:', {
    sent: sentResult.data.stage,
    processed: processedResult.data.stage,
    snapshot: snapshotResult.data.stage,
    projected: projectedResult.data.stage,
  });
}

